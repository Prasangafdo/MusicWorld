Enrique Miguel Iglesias Preysler (born 8 May 1975), known professionally as Enrique Iglesias, is a Spanish singer, songwriter, record producer and actor. He is widely regarded as the King of Latin Pop. Iglesias started his career in the mid-1990s on an American Spanish Language record label Fonovisa Records under the name Enrique Martinez, before switching to the stage name Enrique Iglesias and becoming one of the biggest stars in Latin America and the Hispanic market in the United States. By the turn of the millennium, he had made a successful crossover into the mainstream market and signed a multi-album deal with Universal Music Group for US $68,000,000 with Universal Music Latino to release his Spanish albums and Interscope Records to release English albums. In 2010, he parted with Interscope Records and signed with another Universal Music Group label Republic Records to release bilingual albums. In 2015, Enrique parted ways with Universal Music Group after a decade. He signed with Sony Music; his upcoming albums will be by Sony Music Latin in Spanish and RCA Records in English.

Iglesias has sold over 137 million records worldwide, making him one of the best-selling Spanish artists ever. Iglesias has scored over 150 number-one hits across all of the Billboard charts. He has had five Billboard Hot 100 top five singles, including two number-ones, and holds the record for producing 26 number-one Spanish-language singles on the Billboard's Hot Latin Tracks. He also holds the record for most number-one hits and the longest-running number-one hit on that chart. Iglesias also has 13 number-ones on Billboard's Dance charts, more than any other male artist. He is the best-selling Latin artist in the UK and the best-selling international artist in India.

