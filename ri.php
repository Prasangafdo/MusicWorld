<p style="color:#FCFCFC; font-size:18px">
&nbsp;&nbsp;&nbsp;&nbsp;Robyn Rihanna Fenty (born February 20, 1988) is a Barbadian singer and songwriter. Born in Saint Michael and raised in Bridgetown, she first entered the music industry by recording demo tapes under the direction of record producer Evan Rogers in 2003. She ultimately signed a recording contract with Def Jam Recordings after auditioning for its then-president, hip-hop producer and rapper Jay Z. Rihanna's first two studio albums Music of the Sun (2005) and A Girl like Me (2006) charted on the top 10 of the U.S. Billboard 200 and respectively produced the singles "Pon de Replay" and "SOS".

She assumed creative control of her third studio album Good Girl Gone Bad (2007) and became a household name following the release of its international successful lead single "Umbrella". She followed it with five Recording Industry Association of America (RIAA) platinum-certified albums, including the Grammy Award winning Unapologetic (2012) and her second Billboard 200 number-one album Anti (2016). Many of her songs rank among the world's best-selling singles of all time, including the singles "Umbrella", "Take a Bow", "Disturbia", "Only Girl (In the World)", "S&M", "We Found Love", "Diamonds" and "Stay" in which she is the lead artist, and her collaborations "Live Your Life" (with T.I.), "Love the Way You Lie" and "The Monster" (both with Eminem).

With sales exceeding 200 million records worldwide, Rihanna is one of the best-selling artists of all time. Rihanna is the youngest and fastest solo artist to earn fourteen number-one singles on the Billboard Hot 100, and was named the Digital Songs Artist of the 2000s decade and the top Hot 100 artist of the 2010s decade by Billboard. Among numerous awards and accolades, Rihanna has won eight Grammy Awards; eight American Music Awards; 23 Billboard Music Awards; two BRIT Awards; and was awarded the inaugural Icon Award at the American Music Awards of 2013. Widely recognized for frequently reinventing her style and image, she received the Fashion Icon lifetime achievement award from the Council of Fashion Designers of America in 2014. Forbes ranked Rihanna the fourth most powerful celebrity of 2012, and was named one of Time's 100 Most Influential People in the World later that year.
